def _b(v) -> bool:
    return bool(v)


def build_data(conn):
    vehicles = [
        {
            "vehicleId": r["vehicle_id"], "year": r["year"], "make": r["make"],
            "model": r["model"], "trim": r["trim"], "vehicleClass": r["vehicle_class"],
            "segment": r["segment"], "luxuryFlag": _b(r["luxury_flag"]),
            "powertrain": r["powertrain"], "turboFlag": _b(r["turbo_flag"]),
            "drivetrain": r["drivetrain"],
        }
        for r in conn.execute("SELECT * FROM vehicles ORDER BY vehicle_id")
    ]
    risk = [
        {
            "vehicleId": r["vehicle_id"],
            "brandRepairCost": r["brand_repair_cost"],
            "modelReliability": r["model_reliability"],
            "powertrainComplexity": r["powertrain_complexity"],
            "electronicsComplexity": r["electronics_complexity"],
            "knownFailurePoints": r["known_failure_points"],
            "luxuryPartsCost": r["luxury_parts_cost"],
            "hybridEvComponents": r["hybrid_ev_components"],
            "claimsLikelihood": r["claims_likelihood"],
            "referenceTotal": r["reference_total"],
        }
        for r in conn.execute("SELECT * FROM vehicle_risk_scores ORDER BY vehicle_id")
    ]
    tiers = [
        {"tierKey": r["tier_key"], "name": r["name"], "exclusionaryFlag": _b(r["exclusionary_flag"])}
        for r in conn.execute("SELECT * FROM coverage_tiers ORDER BY tier_key")
    ]
    mileage_bands = [
        {"bandKey": r["band_key"], "minValue": r["min_miles"], "maxValue": r["max_miles"],
         "riskScore": r["risk_score"], "priceMult": r["price_mult"]}
        for r in conn.execute("SELECT * FROM mileage_bands ORDER BY min_miles")
    ]
    age_bands = [
        {"bandKey": r["band_key"], "minValue": r["min_years"], "maxValue": r["max_years"],
         "riskScore": r["risk_score"], "priceMult": r["price_mult"]}
        for r in conn.execute("SELECT * FROM age_bands ORDER BY min_years")
    ]
    observations = [
        {
            "obsId": r["obs_id"], "vehicleId": r["vehicle_id"],
            "vehicleClass": r["vehicle_class"], "coverageTier": r["coverage_tier"],
            "termMonths": r["term_months"], "retailPrice": r["retail_price"],
            "sourceType": r["source_type"],
        }
        for r in conn.execute("SELECT * FROM pricing_observations ORDER BY obs_id")
    ]
    return vehicles, risk, tiers, mileage_bands, age_bands, observations


_HEADER = """// AUTO-GENERATED by scripts/export_ts_data.py — DO NOT EDIT BY HAND.
// Source of truth: database/seed/*.csv (regenerate with: python scripts/export_ts_data.py)

export interface VehicleRow {
  vehicleId: number; year: number; make: string; model: string; trim: string | null;
  vehicleClass: string; segment: string; luxuryFlag: boolean; powertrain: string;
  turboFlag: boolean; drivetrain: string;
}
export interface RiskScoreRow {
  vehicleId: number; brandRepairCost: number; modelReliability: number;
  powertrainComplexity: number; electronicsComplexity: number; knownFailurePoints: number;
  luxuryPartsCost: number; hybridEvComponents: number; claimsLikelihood: number;
  referenceTotal: number;
}
export interface CoverageTierRow { tierKey: string; name: string; exclusionaryFlag: boolean; }
export interface BandRow {
  bandKey: string; minValue: number; maxValue: number | null; riskScore: number; priceMult: number;
}
export interface ObservationRow {
  obsId: number; vehicleId: number | null; vehicleClass: string | null; coverageTier: string;
  termMonths: number | null; retailPrice: number | null; sourceType: string;
}
"""


def _emit_const(name: str, typ: str, rows: list) -> str:
    return f"export const {name}: {typ} = {json.dumps(rows, indent=2)};\n"


def write_data_ts(vehicles, risk, tiers, mileage_bands, age_bands, observations) -> None:
    parts = [_HEADER]
    parts.append(f'export const DATA_VERSION = "0.1.0";\n')
    parts.append(f'export const DATA_GENERATED_AT = "{_dt.date.today().isoformat()}";\n\n')
    parts.append(_emit_const("VEHICLES", "VehicleRow[]", vehicles))
    parts.append(_emit_const("RISK_SCORES", "RiskScoreRow[]", risk))
    parts.append(_emit_const("COVERAGE_TIERS", "CoverageTierRow[]", tiers))
    parts.append(_emit_const("MILEAGE_BANDS", "BandRow[]", mileage_bands))
    parts.append(_emit_const("AGE_BANDS", "BandRow[]", age_bands))
    parts.append(_emit_const("OBSERVATIONS", "ObservationRow[]", observations))
    DATA_TS.parent.mkdir(parents=True, exist_ok=True)
    DATA_TS.write_text("\n".join(parts), encoding="utf-8")


