def _camel_input(d: dict) -> dict:
    return {CAMEL[k]: v for k, v in d.items()}


def build_fixtures(conn) -> dict:
    score_cases = []
    for make, model, miles, years, year in SCORE_CASES:
        s = scoring.score_vehicle(make, model, miles, years, conn=conn, year=year)
        score_cases.append({
            "input": {"make": make, "model": model, "miles": miles, "years": years, "year": year},
            "expected": {
                "fullScore": s.full_score, "intrinsicScore": s.intrinsic_score,
                "mileageScore": s.mileage_score, "ageScore": s.age_score,
                "riskLabel": s.risk_label(), "source": s.source,
                "topDrivers": [SNAKE_TO_CAMEL[d] for d in s.top_drivers],
            },
        })
    for c in SCORE_FALLBACK_CASES:
        s = scoring.score_vehicle(c["make"], c["model"], c["miles"], c["years"], conn=conn, **c["attrs"])
        score_cases.append({
            "input": {"make": c["make"], "model": c["model"], "miles": c["miles"],
                      "years": c["years"], "attrs": c["attrs"]},
            "expected": {
                "fullScore": s.full_score, "intrinsicScore": s.intrinsic_score,
                "mileageScore": s.mileage_score, "ageScore": s.age_score,
                "riskLabel": s.risk_label(), "source": s.source,
                "topDrivers": [SNAKE_TO_CAMEL[d] for d in s.top_drivers],
            },
        })

    price_cases = []
    for kw in PRICE_CASES:
        req = pricing.QuoteRequest(**kw)
        r = pricing.price_quote(req, conn)
        price_cases.append({
            "input": _camel_input(kw),
            "expected": {
                "fairLow": r.fair_low, "fairMid": r.fair_mid, "fairHigh": r.fair_high,
                "dealerCostEst": r.dealer_cost_est, "negotiationTarget": r.negotiation_target,
                "perYearFair": r.per_year_fair, "observedN": r.observed_n,
                "verdict": r.verdict, "markupVsCostPct": r.markup_vs_cost_pct,
                "offerVsFairPct": r.offer_vs_fair_pct, "markupWarning": r.markup_warning,
                "recommendation": r.recommendation, "confidence": r.confidence,
                "fullScore": r.score.full_score,
            },
        })

    return {
        "generatedAt": _dt.date.today().isoformat(),
        "scoreCases": score_cases,
        "priceCases": price_cases,
    }


def main() -> int:
    conn = dbm.build_in_memory()
    try:
        data = build_data(conn)
        write_data_ts(*data)
        fixtures = build_fixtures(conn)
    finally:
        conn.close()

    FIXTURES_JSON.parent.mkdir(parents=True, exist_ok=True)
    FIXTURES_JSON.write_text(json.dumps(fixtures, indent=2) + "\n", encoding="utf-8")

    print(f"wrote {DATA_TS.relative_to(REPO_ROOT)}")
    print(f"wrote {FIXTURES_JSON.relative_to(REPO_ROOT)} "
          f"({len(fixtures['scoreCases'])} score + {len(fixtures['priceCases'])} price cases)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
